<p>Each program should calculate a version of the Fibonacci function using the same na&#239ve recursive-algorithm (Mathematical Handbook 2nd ed., G. Korn, T. Korn, 1968. paragraph 8.7-2).</p>
<pre>
F(n)
  n = 0     = 1
  n = 1     = 1
  otherwise = F(n-2) + F(n-1)
</pre>
<br />

<p>Calculate F(N). Correct output N = 32 is:</p>
<pre>
3524578
</pre>
